                private static readonly Type[] actions;
                private static readonly Type[] funcs;
                static HookHelper() {
                    IEnumerable<Type> invokers = typeof(HookHelper).GetNestedTypes().OrderBy(m => m.GetMethod("Invoke").GetParameters().Length);
                    actions = invokers.Where(m => m.GetMethod("Invoke").ReturnType == typeof(void)).ToArray();
                    funcs = invokers.Where(m => m.GetMethod("Invoke").ReturnType != typeof(void)).ToArray();
                }

                public static Type GetDelegateType(MethodBase signature) {
                    Type returnType = (signature as MethodInfo)?.ReturnType ?? typeof(void);
                    bool isFunc = returnType != typeof(void);

                    ParameterInfo[] args = signature.GetParameters();
                    Type[] argTypes;

                    if (!signature.IsStatic) {
                        argTypes = new Type[args.Length + (isFunc ? 2 : 1)];
                        argTypes[0] = signature.GetThisParamType();
                        for (int i = 0; i < args.Length; i++) {
                            argTypes[i + 1] = args[i].ParameterType;
                        }

                    } else {
                        argTypes = new Type[args.Length + (isFunc ? 1 : 0)];
                        for (int i = 0; i < args.Length; i++) {
                            argTypes[i] = args[i].ParameterType;
                        }
                    }

                    int argsLength = argTypes.Length;
                    if (isFunc)
                        argTypes[argsLength - 1] = returnType;

                    if (argsLength >= actions.Length)
                        return null;

                    Type delegateType = (isFunc ? funcs : actions)[argsLength - (isFunc ? 1 : 0)];
                    if (argsLength == 0)
                        return delegateType;
                    return delegateType.MakeGenericType(argTypes);
                }

